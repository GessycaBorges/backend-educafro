const caixaDeLapisDeCor = ["Azul", "Verde", "Vermelho", "Laranja"];

console.log("---------------------- ARRAY ORIGINAL ---------------------");
console.log(caixaDeLapisDeCor);

console.log("-------------------------- PUSH ---------------------------");
caixaDeLapisDeCor.push("Branco");
console.log(caixaDeLapisDeCor);

console.log("--------------------------- POP ---------------------------");
caixaDeLapisDeCor.pop();
console.log(caixaDeLapisDeCor);

console.log("-------------------------- SHIFT --------------------------");
caixaDeLapisDeCor.shift();
console.log(caixaDeLapisDeCor);

console.log("------------------------- UNSHIFT -------------------------");
caixaDeLapisDeCor.unshift("Preto");
console.log(caixaDeLapisDeCor);